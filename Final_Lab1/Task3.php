<?php
// Define the number
$number = 7;  // Example number (you can change this)

if ($number % 2 == 0) {
    // If the number is divisible by 2, it's even
    echo $number . " is an Even number.";
} else {
    // If the number is not divisible by 2, it's odd
    echo $number . " is an Odd number.";
}
?>
