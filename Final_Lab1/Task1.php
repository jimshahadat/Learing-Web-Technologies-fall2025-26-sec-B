<?php
// Define the length and width of the rectangle
$length = 10;  // Example value for length
$width = 5;    // Example value for width

// Calculate the area and perimeter
$area = $length * $width;
$perimeter = 2 * ($length + $width);

// Display the results
echo "Rectangle Length: " . $length . " units<br>";
echo "Rectangle Width: " . $width . " units<br>";
echo "Area of the Rectangle: " . $area . " square units<br>";
echo "Perimeter of the Rectangle: " . $perimeter . " units<br>";
?>
